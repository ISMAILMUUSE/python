name = "kaamil"
age = 30
print(name == age)  # False, because name is a string and age is an integer this is called equality operator


print( name != age)  # True, because name is not equal to age this is called inequality operator    

print(34 > 56)  # False, because 34 is not greater than 56 this is called greater than operator

print(34 < 56)  # True, because 34 is less than 56 this is called less than operator
 
print(34 >= 34)  # True, because 34 is greater than or equal to 34 this is called greater than or equal to operator

print(34 <= 34)  # True, because 34 is less than or equal to 34 this is called less than or equal to operator

